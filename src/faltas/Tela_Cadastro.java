    private void btnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexcluirActionPerformed
        // TODO add your handling code here:      
            Tabelamodelo model = (Tabelamodelo) tabela.getModel();
            Alunoctrl alunoctrl = new Alunoctrl();
            Aluno cmp = model.getAlunos(tabela.getSelectedRow());
            alunoctrl.deletaAluno(cmp);
            campomatricula.setText("");
            camponomealuno.setText("");
            campocurso.setText("");
            bimestral1.setText("");
            bimestral2.setText("");
            campodisciplina.setText("");
            bimestral3.setText("");
            bimestral4.setText("");
            carregatabela();
            btnsalvar.setEnabled(true);
            
    }//GEN-LAST:event_btnexcluirActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) {
            Tabelamodelo model = (Tabelamodelo) tabela.getModel();
            Aluno cmp = model.getAlunos(tabela.getSelectedRow());
            campomatricula.setText(String.valueOf(cmp.getMatricula()));           
            camponomealuno.setText(cmp.getAluno());
            campodisciplina.setText(cmp.getDisciplina());
            campocurso.setText(cmp.getCurso());
            bimestral1.setText(String.valueOf(cmp.getBimestral1()));
            bimestral2.setText(String.valueOf(cmp.getBimestral2()));
            bimestral3.setText(String.valueOf(cmp.getBimestral3()));
            bimestral4.setText(String.valueOf(cmp.getBimestral4()));
        }
    }//GEN-LAST:event_tabelaMouseClicked

