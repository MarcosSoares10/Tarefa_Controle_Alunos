    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        // TODO add your handling code here:      
            if((!camponomealuno.getText().isEmpty())&&(!campodisciplina.getText().isEmpty())
                    &&(!campomatricula.getText().isEmpty())&&(!camponomealuno.getText().isEmpty())
                    &&(!bimestral1.getText().isEmpty())&&(!bimestral2.getText().isEmpty())
                    &&(!bimestral3.getText().isEmpty())&&(!bimestral4.getText().isEmpty())){             
            Aluno cmp = new Aluno(); //Instancia do Objeto Aluno
            Alunoctrl alunoctrl = new Alunoctrl(); //Controle de Métodos e Conexões
            cmp.setAluno(camponomealuno.getText());
            cmp.setBimestral1(Long.parseLong(bimestral1.getText()));
            cmp.setBimestral2(Long.parseLong(bimestral2.getText()));
            cmp.setBimestral3(Long.parseLong(bimestral3.getText()));
            cmp.setBimestral4(Long.parseLong(bimestral4.getText()));
            cmp.setCurso(campocurso.getText());
            cmp.setDisciplina(campodisciplina.getText());
            cmp.setMatricula(Integer.parseInt(campomatricula.getText()));
            alunoctrl.salvarAluno(cmp);  //adicionar o objeto aluno ao método de controle salvar aluno          
            campocurso.setText("");
            campomatricula.setText("");
            camponomealuno.setText("");
            campodisciplina.setText("");
            bimestral1.setText("");
            bimestral2.setText("");
            bimestral3.setText("");
            bimestral4.setText("");
            }else{System.out.print("Algo Vazio");}
        
            
        
    }//GEN-LAST:event_btnsalvarActionPerformed

